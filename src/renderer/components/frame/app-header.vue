<template>
<m-navbar :theme="theme.theme.headerTheme">
  <m-navbar-brand>
    <i class="side-switch"
      :class="{
        'el-icon-more': !mini,
        'el-icon-more-outline': mini
      }"
      @click="handleSwitchSide"
      ></i>
  </m-navbar-brand>
  <m-nav>
    <el-breadcrumb separator="/" style="line-height: 50px;">
			<el-breadcrumb-item :to="{ path: '/home' }"><span :class="theme.theme.headerColor">首页</span></el-breadcrumb-item>
			<el-breadcrumb-item v-for="(item, index) in $route.meta.nav" :key="index"><span :class="theme.theme.headerColor">{{item}}</span></el-breadcrumb-item>
		</el-breadcrumb>
  </m-nav>
  <m-nav align="right">
    <m-nav-item padding="0">
      <m-dropdown align="right" v-if="userInfo" padding="0 10px">
        <a href="#" style="display: inline-block; padding: 0px; color: inherit;text-decoration: none;">
          <img src="../../assets/user.jpg" alt="" style="border-radius: 3px;vertical-align: middle;">
          <span>{{userInfo.a00102}}</span> 
          <span class="caret"></span>
        </a>
        <m-dropdown-panel>
          <m-dropdown-item><span @click="$router.push('personal')">用户信息</span></m-dropdown-item>
          <m-dropdown-item><span @click="$router.push('personal')">修改密码</span></m-dropdown-item>
          <m-dropdown-item><span @click="logout">退出账号</span></m-dropdown-item>
        </m-dropdown-panel>
      </m-dropdown>
    </m-nav-item>
    <m-nav-item padding="0 5px">
      <a href="#" @click.stop.prevent="handleSwitchScreen">
        <i 
          class="el-icon-rank" 
          :class="isFullScreen ? 'fa-compress' : 'fa-expand'"
          ></i>
      </a>
    </m-nav-item>

    <m-nav-item padding="0">
      <m-dropdown align="right" padding="0 10px">
        <a href="#"  style="padding:0 0px; color: inherit">
          <i class="el-icon-setting"></i>
        </a>
        <m-dropdown-panel style="width: 200px; min-height: 100px;">
          <el-form>
            <el-form-item label="侧边栏">
              <el-switch v-model="showAside" @change="handleSwitchHideSide" />
            </el-form-item>
            <el-form-item label="主题">
              <el-select v-model="themeType" style="width: 100px">
                <el-option
                  v-for="theme in themes"
                  :key="theme.label"
                  :label="theme.label"
                  :value="theme.name"
                  ></el-option>
              </el-select>
            </el-form-item>
          </el-form>
        </m-dropdown-panel>
      </m-dropdown>
    </m-nav-item>
  </m-nav>
</m-navbar>
</template>
<script type="text/javascript">
import {mapActions, mapState} from 'vuex'
import { Logout } from '@/interface/getData'
import {
  requestFullScreen,
  exitFullscreen
} from '@/util'
import themes from './theme'
export default {
  name: 'app-header',
  data () {
    return {
      mini: false,
      isFullScreen: false,
      themes,
      themeType: '',
      showAside: true,
      theme: {theme: {}},
    }
  },
  computed: {
    ...mapState(['userInfo'])
  },
  watch: {
    themeType (val) {
      this.theme = this.themes.find(e => e.name === val) || {}
      this.$emit('set-theme', this.theme)
      localStorage.setItem('theme', val)
    }
  },
  methods: {
    async logout(){   //登出
			const result = await Logout();
			if(result.status == 0){
				this.$router.push('/login');
			}
		},
    handleSwitchSide () {
      this.mini = !this.mini
      this.$emit('switch', this.mini)
    },
    handleSwitchScreen () {
      if (this.isFullScreen) {
        exitFullscreen()
        this.isFullScreen = false
      } else {
        requestFullScreen()
        this.isFullScreen = true
      }
    },
    handleSwitchHideSide () {
      this.$emit('hide-side')
    }
  },
  created () {
    var theme = localStorage.getItem('theme') || 'dark';
    this.themeType = theme
  }
}
</script>
